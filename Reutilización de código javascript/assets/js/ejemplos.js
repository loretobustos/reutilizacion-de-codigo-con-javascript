//Función para sumar dos números
function sumar(a, b) {
    return a + b;
  }
  
  console.log(sumar(5, 3)); // Output: 8
  

//Función para sumar dos números
function calcularAreaCirculo(radio) {
    return Math.PI * radio * radio;
  }
  
  console.log(calcularAreaCirculo(3)); // Output: ~28.27

  
  //Función para verificar si un número es par o impar
  function esPar(numero) {
    return numero % 2 === 0;
  }
  
  console.log(esPar(4)); // Output: true
  console.log(esPar(7)); // Output: false

  
  //Función para generar un saludo personalizado
  function saludar(nombre) {
    return "¡Hola, " + nombre + "!";
  }
  
  console.log(saludar("Juan")); // Output: ¡Hola, Juan!

  

  //Función para encontrar el máximo de un arreglo
  function encontrarMaximo(arr) {
    return Math.max(...arr);
  }
  
  console.log(encontrarMaximo([1, 6, 3, 9, 2])); // Output: 9




  

